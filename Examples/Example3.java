class Example3 {
	public static void main(String args[]) {
		int var = 15;
		double dou = 15;

		System.out.println("Original value of var: " + var);
		System.out.println("Original value of dou: " + dou);
		System.out.println();

		var = var/8;
		dou = dou/8;

		System.out.println("var after division: " + var);
		System.out.println("dou after division:" + dou);
	}
}
